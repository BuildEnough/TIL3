public class _7_문자의_유니코드 {
    public static void main(String[] args) {
        char ch = 'A';
        System.out.println(ch);

        int code = (int)ch;
        System.out.println(code);

        char han = '가';
        System.out.println(han);

        int hcode = (int)han;
        System.out.println(hcode);

    }
}
