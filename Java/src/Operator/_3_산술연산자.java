package Operator;

public class _3_산술연산자 {
    public static void main(String[] args) {
        int a = 10, b = 4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println((float)a /(float)b);
    }
}
